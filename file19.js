// switch case
// break is used to break the loop when the cond is satisfied
let day=3;
switch (day) {
    case 0:
        console.log("Sunday");
        break;
    case 1:
        console.log("Mon");
        break;
    case 2:
        console.log("Tue");
        break;
    case 3:
        console.log("Wed");
        break;
    case 4:
        console.log("Thur");
        break;
    case 5:
        console.log("Fri");
        break;
    case 6:
        console.log("Sat");
        break;

}